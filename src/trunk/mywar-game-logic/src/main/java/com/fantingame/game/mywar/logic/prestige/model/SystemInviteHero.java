package com.fantingame.game.mywar.logic.prestige.model;

/**
 * 声望邀请英雄
 * 
 * @author yezp
 */
public class SystemInviteHero {

	private int systemHeroId;

	private int camp;

	private int needGold;

	private int level;

	public int getSystemHeroId() {
		return systemHeroId;
	}

	public void setSystemHeroId(int systemHeroId) {
		this.systemHeroId = systemHeroId;
	}

	public int getCamp() {
		return camp;
	}

	public void setCamp(int camp) {
		this.camp = camp;
	}

	public int getNeedGold() {
		return needGold;
	}

	public void setNeedGold(int needGold) {
		this.needGold = needGold;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

}
