package com.fantingame.game.mywar.logic.hero.model;

public class SystemHeroName {

	private int id;

	private String malePrefix;

	private String maleSuffix;

	private String femalePrefix;

	private String femaleSuffix;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMalePrefix() {
		return malePrefix;
	}

	public void setMalePrefix(String malePrefix) {
		this.malePrefix = malePrefix;
	}

	public String getMaleSuffix() {
		return maleSuffix;
	}

	public void setMaleSuffix(String maleSuffix) {
		this.maleSuffix = maleSuffix;
	}

	public String getFemalePrefix() {
		return femalePrefix;
	}

	public void setFemalePrefix(String femalePrefix) {
		this.femalePrefix = femalePrefix;
	}

	public String getFemaleSuffix() {
		return femaleSuffix;
	}

	public void setFemaleSuffix(String femaleSuffix) {
		this.femaleSuffix = femaleSuffix;
	}

}
