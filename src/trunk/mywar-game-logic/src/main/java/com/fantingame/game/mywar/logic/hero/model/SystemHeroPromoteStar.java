package com.fantingame.game.mywar.logic.hero.model;

public class SystemHeroPromoteStar {

	private int type;

	private int star;

	private String needMaterial;

	private int lowerNum;

	private int upperNum;

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getStar() {
		return star;
	}

	public void setStar(int star) {
		this.star = star;
	}

	public String getNeedMaterial() {
		return needMaterial;
	}

	public void setNeedMaterial(String needMaterial) {
		this.needMaterial = needMaterial;
	}

	public int getLowerNum() {
		return lowerNum;
	}

	public void setLowerNum(int lowerNum) {
		this.lowerNum = lowerNum;
	}

	public int getUpperNum() {
		return upperNum;
	}

	public void setUpperNum(int upperNum) {
		this.upperNum = upperNum;
	}

}
