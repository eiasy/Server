package com.fantingame.game.mywar.logic.gemstone.model;

public class SystemGemstoneAttr {

	private int gemstoneId;

	private String attr;

	private int lowerNum;

	private int upperNum;

	public int getGemstoneId() {
		return gemstoneId;
	}

	public void setGemstoneId(int gemstoneId) {
		this.gemstoneId = gemstoneId;
	}

	public String getAttr() {
		return attr;
	}

	public void setAttr(String attr) {
		this.attr = attr;
	}

	public int getLowerNum() {
		return lowerNum;
	}

	public void setLowerNum(int lowerNum) {
		this.lowerNum = lowerNum;
	}

	public int getUpperNum() {
		return upperNum;
	}

	public void setUpperNum(int upperNum) {
		this.upperNum = upperNum;
	}

}
