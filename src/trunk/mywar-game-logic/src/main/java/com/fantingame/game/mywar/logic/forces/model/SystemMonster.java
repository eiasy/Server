package com.fantingame.game.mywar.logic.forces.model;

public class SystemMonster {
      private int systemMonsterId;
      private int systemHeroId;
      private int level;
      private int aiId;
      
	public int getSystemMonsterId() {
		return systemMonsterId;
	}
	public void setSystemMonsterId(int systemMonsterId) {
		this.systemMonsterId = systemMonsterId;
	}
	public int getSystemHeroId() {
		return systemHeroId;
	}
	public void setSystemHeroId(int systemHeroId) {
		this.systemHeroId = systemHeroId;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getAiId() {
		return aiId;
	}
	public void setAiId(int aiId) {
		this.aiId = aiId;
	}
}
