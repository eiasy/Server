package com.fantingame.game.mywar.logic.equip.model;

public class SystemEquipFixedAttr {

	private Integer equipId;

	private String mainAttr;

	private String secondaryAttr;

	public Integer getEquipId() {
		return equipId;
	}

	public void setEquipId(Integer equipId) {
		this.equipId = equipId;
	}

	public String getMainAttr() {
		return mainAttr;
	}

	public void setMainAttr(String mainAttr) {
		this.mainAttr = mainAttr;
	}

	public String getSecondaryAttr() {
		return secondaryAttr;
	}

	public void setSecondaryAttr(String secondaryAttr) {
		this.secondaryAttr = secondaryAttr;
	}

}
