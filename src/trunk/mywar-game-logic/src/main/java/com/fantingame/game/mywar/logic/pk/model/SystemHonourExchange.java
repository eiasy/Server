package com.fantingame.game.mywar.logic.pk.model;

/**
 * 荣誉点兑换
 * 
 * @author yezp
 */
public class SystemHonourExchange {

	private int mallId;

	private int needHonour;

	private int toolType;

	private int toolId;

	private int toolNum;

	private int dayBuyNum;

	private int totalBuyNum;

	public int getMallId() {
		return mallId;
	}

	public void setMallId(int mallId) {
		this.mallId = mallId;
	}

	public int getNeedHonour() {
		return needHonour;
	}

	public void setNeedHonour(int needHonour) {
		this.needHonour = needHonour;
	}

	public int getToolType() {
		return toolType;
	}

	public void setToolType(int toolType) {
		this.toolType = toolType;
	}

	public int getToolId() {
		return toolId;
	}

	public void setToolId(int toolId) {
		this.toolId = toolId;
	}

	public int getToolNum() {
		return toolNum;
	}

	public void setToolNum(int toolNum) {
		this.toolNum = toolNum;
	}

	public int getDayBuyNum() {
		return dayBuyNum;
	}

	public void setDayBuyNum(int dayBuyNum) {
		this.dayBuyNum = dayBuyNum;
	}

	public int getTotalBuyNum() {
		return totalBuyNum;
	}

	public void setTotalBuyNum(int totalBuyNum) {
		this.totalBuyNum = totalBuyNum;
	}

}
