package com.fantingame.game.mywar.logic.equip.model;

public class SystemEquipMagic {

	private int reelId;

	private int level;

	private String material;

	public int getReelId() {
		return reelId;
	}

	public void setReelId(int reelId) {
		this.reelId = reelId;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public String getMaterial() {
		return material;
	}

	public void setMaterial(String material) {
		this.material = material;
	}

}
