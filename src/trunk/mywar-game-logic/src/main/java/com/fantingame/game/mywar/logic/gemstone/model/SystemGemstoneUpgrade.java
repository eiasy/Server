package com.fantingame.game.mywar.logic.gemstone.model;

public class SystemGemstoneUpgrade {

	private int gemstoneId;

	private int upgradeGemstoneId;

	private String needMaterial;

	public int getGemstoneId() {
		return gemstoneId;
	}

	public void setGemstoneId(int gemstoneId) {
		this.gemstoneId = gemstoneId;
	}

	public int getUpgradeGemstoneId() {
		return upgradeGemstoneId;
	}

	public void setUpgradeGemstoneId(int upgradeGemstoneId) {
		this.upgradeGemstoneId = upgradeGemstoneId;
	}

	public String getNeedMaterial() {
		return needMaterial;
	}

	public void setNeedMaterial(String needMaterial) {
		this.needMaterial = needMaterial;
	}

}
