package com.fantingame.game.mywar.logic.hero.model;

public class SystemHeroPromote {

	private int systemHeroId;

	private int proSystemHeroId;

	private int heroLevel;

	private String material;

	public int getSystemHeroId() {
		return systemHeroId;
	}

	public void setSystemHeroId(int systemHeroId) {
		this.systemHeroId = systemHeroId;
	}

	public int getProSystemHeroId() {
		return proSystemHeroId;
	}

	public void setProSystemHeroId(int proSystemHeroId) {
		this.proSystemHeroId = proSystemHeroId;
	}

	public int getHeroLevel() {
		return heroLevel;
	}

	public void setHeroLevel(int heroLevel) {
		this.heroLevel = heroLevel;
	}

	public String getMaterial() {
		return material;
	}

	public void setMaterial(String material) {
		this.material = material;
	}

}
