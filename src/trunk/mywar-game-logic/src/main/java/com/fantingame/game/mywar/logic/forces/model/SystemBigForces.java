public class SystemBigForces {

	private int bigForcesId;
	private String bigForcesName;
	private int limitLevel;
	private String imgId;
	private int mapId;
	private int x;
	private int y;

	public int getBigForcesId() {
		return bigForcesId;
	}

	public void setBigForcesId(int bigForcesId) {
		this.bigForcesId = bigForcesId;
	}

	public String getBigForcesName() {
		return bigForcesName;
	}

	public void setBigForcesName(String bigForcesName) {
		this.bigForcesName = bigForcesName;
	}

	public int getLimitLevel() {
		return limitLevel;
	}

	public void setLimitLevel(int limitLevel) {
		this.limitLevel = limitLevel;
	}

	public String getImgId() {
		return imgId;
	}

	public void setImgId(String imgId) {
		this.imgId = imgId;
	}

	public int getMapId() {
		return mapId;
	}

	public void setMapId(int mapId) {
		this.mapId = mapId;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
