package com.fantingame.game.mywar.logic.user.model;

public class SystemTeamExp {
   private int systemTeamLevel;
   private int exp;
   private int battleNum;
   
   public int getSystemTeamLevel() {
		return systemTeamLevel;
   }
   public void setSystemTeamLevel(int systemTeamLevel) {
		this.systemTeamLevel = systemTeamLevel;
   }
   public int getExp() {
		return exp;
   }
   public void setExp(int exp) {
		this.exp = exp;
   }
   public int getBattleNum() {
		return battleNum;
   }
   public void setBattleNum(int battleNum) {
		this.battleNum = battleNum;
   }
}
