package com.fantingame.game.gamecenter.partener.huawei;

public class HwUserInfo {

	private String gender;

	private String userID;

	private String userName;

	private String languageCode;

	private int userState;

	private int userValidStatus;

	private String userTypeFlags;

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getUserID() {
		return userID;
	}

	public void setUserID(String userID) {
		this.userID = userID;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getLanguageCode() {
		return languageCode;
	}

	public void setLanguageCode(String languageCode) {
		this.languageCode = languageCode;
	}

	public int getUserState() {
		return userState;
	}

	public void setUserState(int userState) {
		this.userState = userState;
	}

	public int getUserValidStatus() {
		return userValidStatus;
	}

	public void setUserValidStatus(int userValidStatus) {
		this.userValidStatus = userValidStatus;
	}

	public String getUserTypeFlags() {
		return userTypeFlags;
	}

	public void setUserTypeFlags(String userTypeFlags) {
		this.userTypeFlags = userTypeFlags;
	}

}
