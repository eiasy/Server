package com.framework.client.socket;
/**
 * socket客户端工厂
 * @author ws
 *
 */
public class SocketClientFactory {
  private String ip;
  private int port;
  public SocketClientFactory(String ip,Integer port ){
	  this.ip = ip;
	  this.port = port;
  }
  /**
   * 获取一个客户端
   * @return
   */
  public SocketClient getSocketClient(){
	  return new SocketClient(ip,port);
  }
  
}
